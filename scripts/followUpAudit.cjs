const PLACEHOLDER_RE = [
  /^\d+ research-grade iNaturalist observations at this park\.?$/i,
  /^Recorded \d+ times on iNaturalist at this park\.?$/i,
  /^Appears on \d+% of .+ eBird checklists/i,
  /^Officially documented in the NPS wildlife registry/i,
  /^Recently reported within/i,
  /^Listed in the NPS species inventory/i,
  /^Confirmed at this park's eBird hotspot\.?$/i,
  /^Recorded in this region \(eBird historical checklist\)\.?$/i,
  /^is a (mammal|bird|reptile|amphibian|fish|insect|marine) found at .+ National Park\. It is/i,
];

function isPlaceholder(text) {
  if (!text?.trim()) return true;
  return PLACEHOLDER_RE.some(re => re.test(text.trim()));
}

function needsDesc(animal) {
  if (animal.description && !isPlaceholder(animal.description)) return false;
  if (animal.funFact && !isPlaceholder(animal.funFact)) return false;
  return true;
}

async function task3_fixDescriptions(cache) {
  console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
  console.log('  TASK 3 вЂ” Fix missing descriptions');
  console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');

  // Collect all animals needing descriptions (dedupe by name+sciName)
  const needDesc = new Map(); // key в†’ { name, sciName, locations: [] }
  for (const [parkId, parkData] of Object.entries(cache)) {
    for (const animal of parkData.animals || []) {
      if (!needsDesc(animal)) continue;
      const key = (animal.scientificName || animal.name).toLowerCase();
      if (!needDesc.has(key)) {
        needDesc.set(key, { name: animal.name, sciName: animal.scientificName, locations: [] });
      }
      needDesc.get(key).locations.push({ parkId, animal });
    }
  }

  console.log(`Animals needing descriptions: ${needDesc.size}`);
  if (needDesc.size === 0) { console.log('  вњ“ All animals have descriptions!'); return 0; }

  // List the first 30
  let listed = 0;
  for (const [, info] of needDesc) {
    if (listed++ >= 30) { console.log(`  вЂ¦ and ${needDesc.size - 30} more`); break; }
    console.log(`  - ${info.name} (${info.sciName || 'no sci name'}) @ ${info.locations.length} park(s)`);
  }

  // Fetch Wikipedia descriptions
  console.log(`\nFetching Wikipedia descriptions for ${needDesc.size} speciesвЂ¦`);
  let wikiFound = 0, fallback = 0;

  for (const [key, info] of needDesc) {
    process.stdout.write(`  ${info.name}вЂ¦ `);
    const t = await fetchTaxon(info.name, info.sciName);

    let description = null;
    let descriptionSource = null;

    if (t?.description) {
      description = t.description;
      descriptionSource = 'Wikipedia';
      wikiFound++;
      console.log('вњ“ Wikipedia');
    } else {
      // Build factual template description from the animal name + locations
      const parkNames = [...new Set(info.locations.map(l => l.parkId))].slice(0, 3).join(', ');
      description = `${info.name} is a ${info.locations[0]?.animal?.animalType || 'wildlife'} species documented in US national parks including ${parkNames}.`;
      descriptionSource = 'Park Records';
      fallback++;
      console.log('в†’ template');
    }

    // Apply to all instances in cache
    for (const { animal } of info.locations) {
      if (!animal.description || isPlaceholder(animal.description)) {
        animal.description = description;
        animal.descriptionSource = descriptionSource;
      }
      // Also update photoUrl if we got one and it was missing
      if (t?.photoUrl && !animal.photoUrl) {
        animal.photoUrl = t.photoUrl;
      }
    }
  }

  console.log(`\nDescription enrichment:`);
  console.log(`  Wikipedia descriptions: ${wikiFound}`);
  console.log(`  Template fallbacks:     ${fallback}`);
  console.log(`  Total fixed:            ${wikiFound + fallback}`);

  return wikiFound + fallback;
}

