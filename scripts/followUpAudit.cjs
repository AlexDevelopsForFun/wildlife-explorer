function loadCache() {
  const src = fs.readFileSync(CACHE_PATH, 'utf8');
  const cjs = src
    .replace('export const WILDLIFE_CACHE_BUILT_AT', 'const WILDLIFE_CACHE_BUILT_AT')
    .replace('export const WILDLIFE_CACHE', 'const WILDLIFE_CACHE')
    + '\nmodule.exports = { WILDLIFE_CACHE, WILDLIFE_CACHE_BUILT_AT };';
  const tmp = path.join(os.tmpdir(), '_followup_cache_tmp.cjs');
  fs.writeFileSync(tmp, cjs);
  delete require.cache[require.resolve(tmp)];
  const mod = require(tmp);
  fs.unlinkSync(tmp);
  return { cache: mod.WILDLIFE_CACHE, builtAt: mod.WILDLIFE_CACHE_BUILT_AT };
}

function saveCache(cache, builtAt) {
  const ts = new Date().toISOString();
  let out = `// Auto-generated by scripts/buildWildlifeCache.js вЂ” do not edit manually.\n`;
  out += `// Built: ${builtAt}\n`;
  out += `// Audit follow-up applied: ${ts}\n`;
  out += `// Parks: ${Object.keys(cache).length} | Species bundled: ${Object.values(cache).reduce((s,p)=>s+(p.animals||[]).length,0)}\n\n`;
  out += `export const WILDLIFE_CACHE_BUILT_AT = "${builtAt}";\n\n`;
  out += `export const WILDLIFE_CACHE = ${JSON.stringify(cache, null, 2)};\n\nexport default WILDLIFE_CACHE;\n`;
  fs.writeFileSync(CACHE_PATH, out, 'utf8');
}

// в”Ђв”Ђ HTTP helper в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
function fetchJson(url, headers = {}) {
  return new Promise(resolve => {
    const u = new URL(url);
    const req = https.request({
      hostname: u.hostname,
      path: u.pathname + u.search,
      headers: { 'User-Agent': 'WildlifeMap-AuditFollowUp/1.0', ...headers },
    }, res => {
      let data = '';
      res.on('data', d => data += d);
      res.on('end', () => { try { resolve(JSON.parse(data)); } catch { resolve(null); } });
    });
    req.on('error', () => resolve(null));
    req.setTimeout(15000, () => { req.destroy(); resolve(null); });
    req.end();
  });
}
const sleep = ms => new Promise(r => setTimeout(r, ms));

// в”Ђв”Ђ iNat taxon lookup в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const _taxonCache = {};
async function fetchTaxon(name, sciName) {
  const key = sciName || name;
  if (_taxonCache[key] !== undefined) return _taxonCache[key];
  const q   = encodeURIComponent(sciName || name);
  const url = `https://api.inaturalist.org/v1/taxa?q=${q}&rank=species,subspecies&per_page=3&locale=en`;
  const data = await fetchJson(url);
  await sleep(350);
  if (!data?.results?.length) { _taxonCache[key] = null; return null; }
  let t = data.results.find(x => x.name?.toLowerCase() === (sciName||'').toLowerCase())
       || data.results.find(x => (x.preferred_common_name||'').toLowerCase() === name.toLowerCase())
       || data.results[0];
  const result = {
    photoUrl:    t.default_photo?.medium_url || null,
    description: t.wikipedia_summary || null,
  };
  _taxonCache[key] = result;
  return result;
}

