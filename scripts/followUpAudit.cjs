async function fetchTopMarineForPark(placeId) {
  const results = [];
  for (const taxonGroup of ['Actinopterygii', 'Mollusca', 'Echinodermata']) {
    const url = `https://api.inaturalist.org/v1/observations/species_counts` +
      `?place_id=${placeId}&iconic_taxa[]=${taxonGroup}&quality_grade=research` +
      `&order_by=observations_count&order=desc&per_page=15&locale=en`;
    const data = await fetchJson(url);
    await sleep(600);
    if (!data?.results) continue;
    for (const r of data.results) {
      const obs = r.count ?? 0;
      if (obs < 50) continue; // only well-documented species
      const commonName = r.taxon?.preferred_common_name || r.taxon?.name;
      if (!commonName) continue;
      results.push({
        name: commonName,
        sciName: r.taxon?.name,
        obs,
        taxonGroup,
      });
    }
  }
  // Sort by obs count descending
  results.sort((a, b) => b.obs - a.obs);
  return results;
}

function rarityFromObs(obs, name) {
  const n = name?.toLowerCase() || '';
  // Apply charisma corrections for marine species
  let corrected = obs;
  if (/whale|dolphin|orca|porpoise/.test(n)) corrected = obs / 4;
  else if (/shark/.test(n)) corrected = obs / 2;
  if (corrected >= 2000) return 'guaranteed';
  if (corrected >= 500)  return 'very_likely';
  if (corrected >= 100)  return 'likely';
  if (corrected >= 20)   return 'unlikely';
  if (corrected >= 5)    return 'rare';
  return 'exceptional';
}

