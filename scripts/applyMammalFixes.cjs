const ROOT       = path.join(__dirname, '..');
const CACHE_PATH = path.join(ROOT, 'src', 'data', 'wildlifeCache.js');

// ── Load cache ─────────────────────────────────────────────────────────────────
function loadCache() {
  const src = fs.readFileSync(CACHE_PATH, 'utf8');
  const cjs = src
    .replace('export const WILDLIFE_CACHE_BUILT_AT', 'const WILDLIFE_CACHE_BUILT_AT')
    .replace('export const WILDLIFE_CACHE', 'const WILDLIFE_CACHE')
    .replace('export default WILDLIFE_CACHE;', '')
    + '\nmodule.exports = { WILDLIFE_CACHE, WILDLIFE_CACHE_BUILT_AT };';
  const tmp = path.join(os.tmpdir(), '_mamfix_tmp.cjs');
  fs.writeFileSync(tmp, cjs);
  delete require.cache[require.resolve(tmp)];
  const mod = require(tmp);
  fs.unlinkSync(tmp);
  return { cache: mod.WILDLIFE_CACHE, builtAt: mod.WILDLIFE_CACHE_BUILT_AT };
}

// ── Save cache ─────────────────────────────────────────────────────────────────
function saveCache(cache, builtAt) {
  const ts = new Date().toISOString();
  let out = `// Auto-generated by scripts/buildWildlifeCache.js — do not edit manually.\n`;
  out += `// Built: ${builtAt}\n`;
  out += `// Mammal audit fixes applied: ${ts}\n`;
  out += `// Parks: ${Object.keys(cache).length} | Species bundled: ${Object.values(cache).reduce((s,p)=>s+(p.animals||[]).length,0)}\n\n`;
  out += `export const WILDLIFE_CACHE_BUILT_AT = "${builtAt}";\n\n`;
  out += `export const WILDLIFE_CACHE = ${JSON.stringify(cache, null, 2)};\n\nexport default WILDLIFE_CACHE;\n`;
  fs.writeFileSync(CACHE_PATH, out, 'utf8');
}

// ── Helper: find animal by name (case-insensitive, partial match) ─────────────
function findAnimal(animals, name) {
  const n = name.toLowerCase().trim();
  return animals.find(a => {
    const an = (a.name || '').toLowerCase().trim();
    return an === n || an.includes(n) || n.includes(an);
  });
}

// ── Helper: add animal if not already present ─────────────────────────────────
function addIfMissing(animals, animal, stats) {
  const existing = findAnimal(animals, animal.name);
  if (existing) {
    stats.skipped++;
    return false;
  }
  animals.push(animal);
  stats.added++;
  return true;
}

// ── Helper: fix rarity of existing animal ────────────────────────────────────
function fixRarity(animals, name, newRarity, stats) {
  const a = findAnimal(animals, name);
  if (!a) {
    stats.notFound++;
    return false;
  }
  if (a.rarity === newRarity) return false;
  const old = a.rarity;
  a.rarity = newRarity;
  stats.rarityFixed++;
  stats.rarityLog.push(`  ${a.name}: ${old} → ${newRarity}`);
  return true;
}

// ── Helper: remove season from existing animal ────────────────────────────────
function removeSeason(animals, namePredicate, seasonToRemove, stats) {
  let count = 0;
  for (const a of animals) {
    if (!namePredicate(a.name)) continue;
    if (!a.seasons?.includes(seasonToRemove)) continue;
    a.seasons = a.seasons.filter(s => s !== seasonToRemove);
    stats.seasonFixed++;
    stats.seasonLog.push(`  ${a.name}: removed ${seasonToRemove}`);
    count++;
  }
  return count;
}

