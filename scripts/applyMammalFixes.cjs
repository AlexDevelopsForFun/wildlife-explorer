  for (const parkId of SQUIRREL_NO_WINTER_PARKS) {
    if (!cache[parkId]) continue;
    const n = removeSeason(cache[parkId].animals, isHibernatingSquirrel, 'winter', stats);
    if (n > 0) console.log(`  [${parkId}] removed winter from ${n} ground squirrel/marmot(s)`);
  }

