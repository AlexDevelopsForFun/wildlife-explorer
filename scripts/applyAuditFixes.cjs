// ── load .env ──────────────────────────────────────────────────────────────
function loadEnv() {
  const env = {};
  try {
    for (const line of fs.readFileSync(path.join(ROOT, '.env'), 'utf8').split('\n')) {
      const m = line.match(/^\s*([^#=]+?)\s*=\s*(.*?)\s*$/);
      if (m) env[m[1]] = m[2].replace(/^["']|["']$/g, '');
    }
  } catch {}
  return env;
}
const ENV = loadEnv();

// ── load / save wildlifeCache ──────────────────────────────────────────────
function loadCache() {
  const src = fs.readFileSync(path.join(ROOT, 'src', 'data', 'wildlifeCache.js'), 'utf8');
  const cjs = src
    .replace('export const WILDLIFE_CACHE_BUILT_AT', 'const WILDLIFE_CACHE_BUILT_AT')
    .replace('export const WILDLIFE_CACHE', 'const WILDLIFE_CACHE')
    + '\nmodule.exports = { WILDLIFE_CACHE, WILDLIFE_CACHE_BUILT_AT };';
  const tmp = path.join(__dirname, '_fix_cache_tmp.cjs');
  fs.writeFileSync(tmp, cjs);
  const mod = require(tmp);
  fs.unlinkSync(tmp);
  return { cache: mod.WILDLIFE_CACHE, builtAt: mod.WILDLIFE_CACHE_BUILT_AT };
}

function saveCache(cache, builtAt) {
  const ts = new Date().toISOString();
  let out = `// Auto-generated by scripts/buildWildlifeCache.js — do not edit manually.\n`;
  out += `// Built: ${builtAt}\n`;
  out += `// Audit fixes applied: ${ts}\n`;
  out += `// Parks: ${Object.keys(cache).length} | Species bundled: ${Object.values(cache).reduce((s,p)=>s+(p.animals||[]).length,0)}\n\n`;
  out += `export const WILDLIFE_CACHE_BUILT_AT = "${builtAt}";\n\n`;
  out += `export const WILDLIFE_CACHE = ${JSON.stringify(cache, null, 2)};\n\nexport default WILDLIFE_CACHE;\n`;
  fs.writeFileSync(path.join(ROOT, 'src', 'data', 'wildlifeCache.js'), out, 'utf8');
}

// ── HTTP helper ────────────────────────────────────────────────────────────
function fetchJson(url, headers = {}) {
  return new Promise((resolve) => {
    const u = new URL(url);
    const req = https.request({
      hostname: u.hostname,
      path: u.pathname + u.search,
      headers: { 'User-Agent': 'WildlifeMap-AuditFix/1.0', ...headers },
    }, res => {
      let data = '';
      res.on('data', d => data += d);
      res.on('end', () => { try { resolve(JSON.parse(data)); } catch { resolve(null); } });
    });
    req.on('error', () => resolve(null));
    req.setTimeout(15000, () => { req.destroy(); resolve(null); });
    req.end();
  });
}
const sleep = ms => new Promise(r => setTimeout(r, ms));

// ── fetch iNat taxon for photo + description ───────────────────────────────
const taxonCache = {};
async function fetchTaxon(name, sciName) {
  const key = sciName || name;
  if (taxonCache[key]) return taxonCache[key];
  const q = sciName || name;
  const url = `https://api.inaturalist.org/v1/taxa?q=${encodeURIComponent(q)}&rank=species,subspecies&per_page=3`;
  const data = await fetchJson(url);
  await sleep(400);
  if (!data?.results?.length) { taxonCache[key] = null; return null; }
  // prefer exact sci name match, then common name match, else first result
  let taxon = data.results.find(t => t.name?.toLowerCase() === (sciName||'').toLowerCase())
    || data.results.find(t => (t.preferred_common_name||'').toLowerCase() === name.toLowerCase())
    || data.results[0];
  const result = {
    photoUrl:    taxon.default_photo?.medium_url || null,
    photoCredit: taxon.default_photo?.attribution || null,
    description: taxon.wikipedia_summary || null,
    taxonId:     taxon.id,
  };
  taxonCache[key] = result;
  return result;
}

