// ── ─────────────────────────────────────────────────────────────────────────
async function main() {
  console.log('══════════════════════════════════════════════════');
  console.log('  Applying Priority 1 + 2 Audit Fixes');
  console.log('══════════════════════════════════════════════════\n');

  const { cache, builtAt } = loadCache();

  let rarityFixed = 0;
  let animalsAdded = 0;
  const addedList = [];

  // ── Step 1: Apply rarity fixes to existing animals ─────────────────────
  console.log('Step 1 — Applying rarity/season fixes to existing animals…');
  for (const fix of RARITY_FIXES) {
    const park = cache[fix.park];
    if (!park?.animals) { console.log(`  ⚠ Park not found: ${fix.park}`); continue; }
    const animal = park.animals.find(a => a.name.toLowerCase() === fix.name.toLowerCase());
    if (!animal) { console.log(`  ⚠ Animal not found: ${fix.name} @ ${fix.park}`); continue; }
    const old = animal.rarity;
    animal.rarity = fix.rarity;
    if (fix.seasons) animal.seasons = fix.seasons;
    console.log(`  ✓ ${fix.name} @ ${fix.park}: ${old} → ${fix.rarity}${fix.seasons ? ' seasons='+fix.seasons.join(',') : ''}`);
    rarityFixed++;
  }

  // ── Step 2: Prefetch iNat taxon data for all unique new species ────────
  console.log('\nStep 2 — Fetching iNat taxon data (photos + descriptions)…');
  const uniqueSpecies = [...new Set(NEW_ANIMALS.map(a => a.sciName || a.name))];
  const taxonMap = {};
  for (const sp of uniqueSpecies) {
    const animalDef = NEW_ANIMALS.find(a => (a.sciName || a.name) === sp);
    process.stdout.write(`  ${animalDef?.name || sp}… `);
    const t = await fetchTaxon(animalDef?.name || sp, sp);
    if (t) {
      taxonMap[sp] = t;
      console.log(`✓ photo=${!!t.photoUrl} desc=${t.description?.length || 0}chars`);
    } else {
      console.log('⚠ no taxon data');
    }
  }

  // ── Step 3: Add new animals to parks ──────────────────────────────────
  console.log('\nStep 3 — Adding new animals to parks…');
  for (const animalDef of NEW_ANIMALS) {
    const taxon = taxonMap[animalDef.sciName || animalDef.name];
    // Use Wikipedia description if available, else use funFact as fallback description
    const description = taxon?.description || animalDef.funFact;

    for (const parkEntry of animalDef.parks) {
      const park = cache[parkEntry.id];
      if (!park) { console.log(`  ⚠ Park not found: ${parkEntry.id}`); continue; }
      // Check not already present
      const exists = park.animals.some(a => a.name.toLowerCase() === animalDef.name.toLowerCase());
      if (exists) {
        // Update rarity/seasons if exists (e.g. American Crocodile @ Biscayne we add separately)
        const existing = park.animals.find(a => a.name.toLowerCase() === animalDef.name.toLowerCase());
        if (existing && parkEntry.rarity !== existing.rarity) {
          const old = existing.rarity;
          existing.rarity = parkEntry.rarity;
          existing.seasons = parkEntry.seasons;
          console.log(`  ↑ updated ${animalDef.name} @ ${parkEntry.id}: ${old} → ${parkEntry.rarity}`);
        }
        continue;
      }

      const newAnimal = {
        name:           animalDef.name,
        emoji:          animalDef.emoji,
        animalType:     animalDef.type,
        rarity:         parkEntry.rarity,
        seasons:        parkEntry.seasons,
        scientificName: animalDef.sciName || null,
        funFact:        animalDef.funFact,
        photoUrl:       taxon?.photoUrl || null,
        source:         'inaturalist',
        sources:        ['inaturalist'],
        description:    description,
        descriptionSource: taxon?.description ? 'Wikipedia' : 'Park Records',
      };

      park.animals.push(newAnimal);
      animalsAdded++;
      addedList.push(`${animalDef.name} @ ${parkEntry.id} (${parkEntry.rarity})`);
      console.log(`  + ${animalDef.name} @ ${parkEntry.id} [${parkEntry.rarity}]`);
    }
  }

  // ── Step 4: Save cache ─────────────────────────────────────────────────
  console.log('\nStep 4 — Writing updated wildlifeCache.js…');
  saveCache(cache, builtAt);
  const totalAnimals = Object.values(cache).reduce((s,p)=>s+(p.animals||[]).length,0);
  console.log(`  ✓ Written: ${totalAnimals} total animals (was 28,601)`);

  // ── Summary ────────────────────────────────────────────────────────────
  console.log('\n══════════════════════════════════════════════════');
  console.log(`  Rarity fixes applied: ${rarityFixed}`);
  console.log(`  New animals added:    ${animalsAdded}`);
  console.log(`  Total animals now:    ${totalAnimals}`);
  console.log('══════════════════════════════════════════════════');
  console.log('\nAdded:');
  addedList.forEach(a => console.log('  ' + a));
}

main().catch(err => { console.error('Fix script failed:', err); process.exit(1); });
